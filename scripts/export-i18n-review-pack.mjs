function csvEscape(s) {
  const v = String(s ?? "");
  if (/[",\n]/.test(v)) return `"${v.replace(/"/g, '""')}"`;
  return v;
}

fs.mkdirSync(outDir, { recursive: true });

const targets = ["th", "my", "ne", "ar"];
for (const code of targets) {
  I18n.setLocale("en");
  const rows = [["key", "english", "current_draft", "corrected_by_native", "notes"]];
  for (const key of KEYS) {
    I18n.setLocale("en");
    const en = I18n.t(key);
    I18n.setLocale(code);
    const cur = I18n.t(key);
    const sameAsEn = cur === en ? "YES — still English fallback" : "";
    rows.push([key, en, cur, "", sameAsEn]);
  }
  const body = rows.map((r) => r.map(csvEscape).join(",")).join("\n") + "\n";
  const file = path.join(outDir, `review-${code}.csv`);
  fs.writeFileSync(file, body, "utf8");
  console.log("Wrote", file);
}

