/**
 * Export dashboard i18n strings for native-speaker review (Blok F).
 * Usage: node scripts/export-i18n-review-pack.mjs
 * Writes docs/i18n-review/*.csv for th, my, ne, ar.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import vm from "node:vm";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const i18nPath = path.join(root, "artifacts/api-server/public/dashboard/i18n.js");
const outDir = path.join(root, "docs/i18n-review");

const code = fs.readFileSync(i18nPath, "utf8");
const fakeEl = () => ({
  style: {},
  textContent: "",
  value: "",
  getAttribute: () => null,
  setAttribute: () => {},
  innerHTML: "",
});
const sandbox = {
  window: {},
  console,
  document: {
    documentElement: { lang: "en", dir: "ltr" },
    querySelectorAll: () => [],
    getElementById: () => fakeEl(),
  },
  navigator: { language: "en" },
  localStorage: {
    getItem: () => null,
    setItem: () => {},
  },
};
vm.runInNewContext(code, sandbox);
const I18n = sandbox.window.OrderlyI18n;
if (!I18n) {
  console.error("Failed to load OrderlyI18n from i18n.js");
  process.exit(1);
}

// Re-read dict by evaluating again with capture — LOCALES + internal dict not fully exported.
// So we parse keys from English via apply + t for each known key list from HTML + en pack.
